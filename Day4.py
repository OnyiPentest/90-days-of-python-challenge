print ("\nA SIMPLE PYTHON COUNTDOWN PROGRAM USING WHILE LOOP\n")

# A COUNTDOWN FROM 10 TO 0
count = 10

while count > 0:
    print(count)
    count -= 1 # Count Decreases by 1

print ("Count Down completed")
